"""LLM client for Coastal chat — Supernemotron via OpenRouter.

Owner directive 2026-04-30: customer-facing chat replies are generated by
NVIDIA's Llama-3.3 Nemotron Super 49B v1, accessed through the OpenRouter
gateway. Falls back gracefully to the rule-based `_navigate_chat` state
machine in api_server.py if OpenRouter is unreachable, the API key is
unset, or the model returns an empty reply — so the chat surface never
returns empty content again (the bug we just patched).

Reuses Sal_Ang's INSTRUCTION block (agents/operations_pmo/sal_ang/agent.py)
and the BRAND_GUARDRAILS / CLAIMS_VOIDER_BLOCK from
agents/shared/coastal_context.py — single source of truth for posture +
forbidden-claim list.
"""
from __future__ import annotations

import os
import time
from typing import Iterable, Optional

import requests

OPENROUTER_API_KEY = os.environ.get("OPENROUTER_API_KEY", "")
OPENROUTER_BASE = os.environ.get(
    "OPENROUTER_BASE_URL", "https://openrouter.ai/api/v1"
)
# Supernemotron — NVIDIA's Llama-3.3 Nemotron Super 49B v1.5 (instruct-tuned,
# non-reasoning — outputs clean answers without chain-of-thought leakage).
# Owner can override via COASTAL_CHAT_MODEL. The Nemotron-3 Super 120B
# reasoning model leaks scratchpad even with `reasoning.exclude` on the
# free tier, so we default to the cleaner instruct-tuned model.
DEFAULT_MODEL = os.environ.get(
    "COASTAL_CHAT_MODEL", "nvidia/llama-3.3-nemotron-super-49b-v1.5"
)

DEFAULT_TIMEOUT_S = 25.0
MAX_HISTORY_TURNS = 6  # token budget guard — last 6 user/agent turns
# Llama-3.3 Nemotron Super 49B v1.5 is a reasoning model — it produces an
# internal scratchpad before the final answer. Budget = ~800 reasoning + 280
# answer + buffer. Final answer pulled from message.content; reasoning
# scratchpad is in message.reasoning and discarded.
MAX_REPLY_TOKENS = 1200


def is_configured() -> bool:
    return bool(OPENROUTER_API_KEY)


def _system_prompt(agent: str) -> str:
    """Customer-facing voice is ACHEEVY for all lanes. Lane key tunes
    the internal-context block but the persona, voice, and identity
    remain ACHEEVY per CLAUDE.md root rule "Only ACHEEVY speaks to users"
    and `feedback_only_acheevy_speaks_to_users_on_coastal_chat.md`.
    """
    if agent == "marketing":
        return _ACHEEVY_SYSTEM_PROMPT + "\n\n" + _MARKETING_LANE_CONTEXT
    return _ACHEEVY_SYSTEM_PROMPT + "\n\n" + _SALES_LANE_CONTEXT


def chat_completion(
    user_message: str,
    history: Optional[Iterable[dict]] = None,
    agent: str = "sales",
    model: Optional[str] = None,
) -> dict:
    """Round-trip a message through OpenRouter.

    Returns a dict shaped:
        {
          "ok": bool,
          "content": str | None,    # the assistant reply
          "model": str,
          "latency_ms": int,
          "error": str | None,
        }
    Caller decides how to fall back if `ok` is False.
    """
    if not is_configured():
        return {
            "ok": False,
            "content": None,
            "model": model or DEFAULT_MODEL,
            "latency_ms": 0,
            "error": "OPENROUTER_API_KEY not set",
        }

    messages = [{"role": "system", "content": _system_prompt(agent)}]

    if history:
        # Keep only the last MAX_HISTORY_TURNS user+assistant pairs.
        trimmed = list(history)[-MAX_HISTORY_TURNS * 2 :]
        for entry in trimmed:
            role = entry.get("role")
            content = entry.get("content") or ""
            if role in ("user", "assistant") and content:
                messages.append({"role": role, "content": content})

    messages.append({"role": "user", "content": user_message})

    payload = {
        "model": model or DEFAULT_MODEL,
        "messages": messages,
        "max_tokens": MAX_REPLY_TOKENS,
        "temperature": 0.6,
        # Suppress Nemotron-3 reasoning chain-of-thought from the response —
        # we want the customer-facing answer only, not the raw scratchpad.
        "reasoning": {"exclude": True},
    }

    headers = {
        "Authorization": f"Bearer {OPENROUTER_API_KEY}",
        "Content-Type": "application/json",
        # OpenRouter recommends these for routing + analytics.
        "HTTP-Referer": "https://brewing.foai.cloud",
        "X-Title": "Coastal Brewing Co.",
    }

    started = time.monotonic()
    try:
        resp = requests.post(
            f"{OPENROUTER_BASE}/chat/completions",
            json=payload,
            headers=headers,
            timeout=DEFAULT_TIMEOUT_S,
        )
        latency_ms = int((time.monotonic() - started) * 1000)
        if resp.status_code != 200:
            return {
                "ok": False,
                "content": None,
                "model": payload["model"],
                "latency_ms": latency_ms,
                "error": f"openrouter_http_{resp.status_code}: {resp.text[:300]}",
            }
        data = resp.json()
        choices = data.get("choices") or [{}]
        message = choices[0].get("message") or {}
        raw_content = message.get("content")
        # Some OpenRouter models return content=None when output is empty
        # or filtered. Coerce to empty string before strip().
        content = (raw_content or "").strip() if isinstance(raw_content, str) else ""
        if not content:
            return {
                "ok": False,
                "content": None,
                "model": payload["model"],
                "latency_ms": latency_ms,
                "error": "openrouter_empty_response",
            }
        return {
            "ok": True,
            "content": content,
            "model": payload["model"],
            "latency_ms": latency_ms,
            "error": None,
        }
    except requests.RequestException as e:
        return {
            "ok": False,
            "content": None,
            "model": payload["model"],
            "latency_ms": int((time.monotonic() - started) * 1000),
            "error": f"openrouter_request_error: {e}",
        }


